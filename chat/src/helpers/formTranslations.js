export function generateTranslations(lang) {
  var res = {};
  for (const [key, value] of Object.entries(lang)) {
    res[key.toLowerCase()] = value.toLowerCase();
    res[key.toUpperCase()] = value.toUpperCase();
    res[
      `${key[0].toUpperCase()}${key.slice(1).toLowerCase()}`
    ] = `${value[0].toUpperCase()}${value.slice(1).toLowerCase()}`;
    res[
      key
        .toLowerCase()
        .split(' ')
        .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ')
    ] = value
      .toLowerCase()
      .split(' ')
      .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
      .join(' ');
  }
  console.log('generateTranslations', { ...lang, ...res });
  return { ...lang, ...res };
}

